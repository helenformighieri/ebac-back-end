package tarefa_calculo_media;

public class CalculoMedia {
    public static void main(String[] args) {
        float notaUm = 8.5F;
        float notaDois = 9;
        float notaTres = 7.5F;
        float notaQuatro = 10;
        float media = (notaUm + notaDois + notaTres + notaQuatro)/4;

        System.out.println("Sua média é: " + media);
    }
}
